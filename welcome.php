                  <p>Peramalan (<em>forecasting</em>) merupakan alata bantu yang penting dalam perencanaan yang efektif dan efisien khususnya dalam bidang ekonomi. Dalam organisasi modern mengetahui keadaan yang akan datang tidak saja penting untuk melihat yang baik atau yang buruk tetapi juga untuk melakukan persiapan peramalan. </p>
